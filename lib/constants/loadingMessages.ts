// 랜덤 메시지 선택 함수
export function getRandomLoadingMessage(type: keyof typeof LOADING_MESSAGES = 'apartmentInfo'): string {
    const messages = LOADING_MESSAGES[type];
    return messages[Math.floor(Math.random() * messages.length)];
}

// 로딩 메시지를 섞어서 표시 (정보 + 팁)
export function getLoadingWithTip(mainType: keyof typeof LOADING_MESSAGES = 'apartmentInfo'): {
    main: string;
    tip: string;
} {
    return {
        main: getRandomLoadingMessage(mainType),
        tip: getRandomLoadingMessage('tips')
    };
}
